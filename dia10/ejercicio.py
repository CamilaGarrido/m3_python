# Crear un diccionario
mi_diccionario = {
    "nombre": "Camila",
    "edad": 25,
    "ciudad": "Concepción"
}

# Imprimir el diccionario original
print(mi_diccionario)

# Agregar un elemento
mi_diccionario["ocupacion"] = "estudiante"

# Imprimir el diccionario con el nuevo elemento
print(mi_diccionario)

# Cambiar un elemento
mi_diccionario["edad"] = 26

# Imprimir el diccionario con el cambio de edad
print(mi_diccionario)

# Eliminar un elemento
del mi_diccionario["ciudad"]

# Imprimir el diccionario después de eliminar la ciudad
print(mi_diccionario)
